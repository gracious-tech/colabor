h2 Can I give towards a particular activity of a fundraiser?

p You can only give towards the entire fundraiser, not specific activities within that fundraiser. But you can show your appreciation of a particular activity by selecting it when donating.

p The reason for this is that it's an administrative burden to divide donations by specific causes, and it can also limit the ability for a ministry to function if some parts of it end up being more funded than others. In order to avoid burdening ministries in these ways, we don't support giving towards a particular activity.


