h2 Misconceptions about the accountability organisations provide

p Organisational structures can be helpful, but their strengths are often outweighed by their weaknesses when applied to small ministries. Small ministries being the focus here.

p What do non-profits actually provide? And how does that compare with directly supporting someone?


h3 Organisations ensure funds are kept track of

p The most basic form of accountability is actually keeping track of how much money is coming in. Organisations usually have someone dedicated to this task, carefully keeping account of all income and expenditure.

p However, there is no reason why an individual cannot do this themselves. Small ministries often have so few transactions that they can fit on a single page, so extensive accounting is not necessary.

div.cs-negative
    div Excessive accounting wastes money
    p Simon helps the poor in his neighbourhood. He's heard of financial abuse in other charities and wants to make sure his work is above reproach, so he starts a non-profit and does an independent audit each year. He manages to raise $30,000 in donations, but $5,000 of that is spent on the independent audit each year, and another $5,000 for other admin, leaving only 66% for giving to the poor.


h3 Organisations ensure secure means of payment

p It used to be the case that securely processing payments (especially card payments) online was something reserved for organisations. But thanks to new online services, like Stripe and PayPal, anyone can securely accept online payment without having to handle the details themselves.

p While some organisations claim that card payments are the best way to donate, they are not the most efficient for donors. Some organisations prefer donors paying by card so that they can be in control of recurring payments rather than donors, even if transaction fees are higher. Scheduled bank transfers on the other hand are often surcharge free and keep donors in control of their own donations.

