h2 A reasonable alternative for small ministries


p As we've seen, organisations can lower risk with the accountability they provide, but they do not eliminate it and they lower it at a cost; A cost that is not always appropriate for low risk scenarios. We've also seen that there is nothing inherently wrong with directly giving someone money, and it is much more efficient in terms of both time and expenses for small ministries. There is not currently a culture of giving to all the various forms of ministry possible and so many have instead turned to commercialising their ministry to get by.

p We propose that Christians should give wholeheartedly to small ministries without expecting the same level of admin as organisations.

