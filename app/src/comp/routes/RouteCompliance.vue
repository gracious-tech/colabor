h3 Organisations ensure the ministry is non-profit

p Some Christian organisations are actually for-profit and it can be hard to know which as they rarely make this obvious. Regarding organisations that are set up as non-profits, it's important to realise this is a legal distinction and not an entirely biblical one. A non-profit organisation can only use its funds to further the cause it was set up for, but there are few other restrictions and many executives personally profit through high salaries.

p It is common practice for Christian organisations to compare to each other when setting executive pay, or even compare to the secular world. In other words, the policy is "we'll do what everyone else is doing". As a result there are Christian CEOs that receive over $400k USD/year (Compassion), $800k (Samaritan's Purse), or even $7 million (Inspiration Ministries). While the latter is associated with false teachers, it still goes to show that it is legal to pay incredibly high salaries for non-profit executives.

