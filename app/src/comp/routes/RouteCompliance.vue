h3 Organisations ensure someone's beliefs are biblical

p While most organisations have a statement of beliefs, they usually only cover the very basics of the faith. There is nothing stopping staff from holding unbiblical beliefs that they simply don't make known or don't make a big deal of. Since organisations often provide attractive employment opportunities, there is the added incentive to hide such beliefs. Likewise, in regard to donors, the organisation has an incentive to attract donors from as large a pool of believers as possible.

p Individuals and small ministries, on the other hand, may not even publish a statement of beliefs, but it is far more likely that donors know them personally. Most small ministries will primarily be supported by friends and family of those doing the ministry; people who know the person's beliefs very well.

